import Foundation
import UIKit

extension UIFont {
    
    class func regular(sz: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Regular", size: sz)!
    }

    class func medium(sz: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Medium", size: sz)!
    }

    class func blackItalic(sz: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-BlackItalic", size: sz)!
    }

    class func bold(sz: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Bold", size: sz)!
    }

    class func light(sz: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Light", size: sz)!
    }
}
